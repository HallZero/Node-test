const sum = function(a, b){
    return a + b;
}

const sub = function(a, b){
    return a - b;
}

const times = function(a, b){
    return a * b;
}

const div = function(a, b){
    return a / b;
}

module.exports = sum;
module.exports = sub;
module.exports = times;
module.exports = div;

